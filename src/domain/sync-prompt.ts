// src/domain/sync-prompt.ts
import { SPINE_HEADINGS } from './types';

export function buildSyncPrompt(
  currentSpecMarkdown: string,
  transcriptExcerpt: string,
  gitDiff: string,
): string {
  return [
    '너는 사용자가 만들고 있는 서비스를 *사용자에게 설명하는 제품 문서*(doc.md)를 유지하는 스크라이브다.',
    '사용자는 자기 터미널에서 직접 코딩 중이고, 아래는 최근 활동 발췌와 코드 변경(git diff)이다.',
    '이 문서는 "무슨 작업을 했는지"(작업 로그)가 아니다. 그건 히스토리의 몫이다.',
    '대신 *사용자 관점에서 각 기능/페이지가 무엇이고 어떻게 동작하는지* — 시스템 내부 구현이 아니라 사용자에게 보이는 동작·정책·요소·상태 — 를 기록한다.',
    '규칙:',
    `1) "## 개요"(서비스가 무엇인지·누구를 위한 것인지, 사용자 관점 1~2문단)와 "## 열린 질문"은 항상 유지한다. (스파인: ${SPINE_HEADINGS.join(' , ')})`,
    '2) 기능/페이지마다 "## <이름>" 섹션을 만든다. 각 섹션엔 **무엇**(한 줄), **동작·정책**(규칙), **요소**(화면 구성), **상태**(유휴/오류 등 해당될 때)를 적는다.',
    '3) 대화·코드에서 새로 구현되거나 정해진 *제품 동작*만 반영해 해당 섹션을 추가/갱신한다.',
    '4) 작업 과정·할 일·커밋 메시지 같은 작업 로그는 절대 쓰지 않는다(그건 히스토리).',
    '5) 아직 정해지지 않았거나 모순된 것은 "## 열린 질문"에 - 불릿으로 모은다.',
    '6) 추측으로 없는 기능을 지어내지 말고, 대화·diff에 근거한 것만 반영한다.',
    '',
    '현재 문서:',
    '"""',
    currentSpecMarkdown,
    '"""',
    '',
    '최근 대화 발췌:',
    '"""',
    transcriptExcerpt || '(없음)',
    '"""',
    '',
    '코드 변경(git diff):',
    '"""',
    gitDiff || '(없음)',
    '"""',
    '',
    '갱신된 문서 마크다운 "전체"만 출력하라. 설명·코드펜스(```) 없이.',
  ].join('\n');
}
